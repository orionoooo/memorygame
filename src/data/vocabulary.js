// Helper to get all words as a flat array
export function getAllWords() {
  return Object.values(vocabulary).flat()
}

// Helper to get random words from a category or all
export function getRandomWords(count, category = null) {
  const words = category ? vocabulary[category] : getAllWords()
  const shuffled = [...words].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

// Helper to shuffle an array
export function shuffleArray(array) {
  const newArray = [...array]
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[newArray[i], newArray[j]] = [newArray[j], newArray[i]]
  }
  return newArray
}

